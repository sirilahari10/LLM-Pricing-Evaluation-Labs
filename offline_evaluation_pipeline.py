import numpy as np
import pandas as pd
from scipy import stats
import matplotlib.pyplot as plt

# 1. Simulate Offline Lab Data (Amazon PPE Context)
# Evaluating two LLM variants on predicting competitive product prices
np.random.seed(42)
n_products = 10000

# True market price of the products
true_prices = np.random.lognormal(mean=3.5, sigma=1.0, size=n_products)

# Model A (Baseline LLM): Higher variance, occasional large hallucinations
model_a_preds = true_prices * np.random.normal(1.05, 0.20, n_products)

# Model B (Agentic LLM with Tool-Use): Tighter variance, fewer hallucinations
model_b_preds = true_prices * np.random.normal(1.01, 0.10, n_products)

df = pd.DataFrame({
    'product_id': range(n_products),
    'true_price': true_prices,
    'model_a_pred': model_a_preds,
    'model_b_pred': model_b_preds
})

# 2. Calculate Evaluation Metric: MAPE (Mean Absolute Percentage Error)
def calculate_mape(true, pred):
    return np.mean(np.abs((true - pred) / true)) * 100

mape_a = calculate_mape(df['true_price'], df['model_a_pred'])
mape_b = calculate_mape(df['true_price'], df['model_b_pred'])

print(f"Model A Baseline MAPE: {mape_a:.2f}%")
print(f"Model B Agentic MAPE: {mape_b:.2f}%")

# 3. Statistical Rigor: Bootstrapped Confidence Intervals
# In Amazon scale, we must prove the improvement is statistically significant
def bootstrap_mape_diff(true, pred_a, pred_b, n_iterations=1000):
    diffs = []
    for _ in range(n_iterations):
        indices = np.random.randint(0, len(true), len(true))
        sample_true = true.iloc[indices]
        sample_a = pred_a.iloc[indices]
        sample_b = pred_b.iloc[indices]

        diff = calculate_mape(sample_true, sample_a) - calculate_mape(sample_true, sample_b)
        diffs.append(diff)
    return np.percentile(diffs, [2.5, 97.5]), diffs

ci, diff_distribution = bootstrap_mape_diff(df['true_price'], df['model_a_pred'], df['model_b_pred'])

print(f"95% CI for MAPE Improvement (Model A - Model B): [{ci[0]:.2f}%, {ci[1]:.2f}%]")

# 4. Generate Proof Visualization
plt.figure(figsize=(8, 5))
plt.hist(diff_distribution, bins=50, color='orange', alpha=0.7)
plt.axvline(ci[0], color='red', linestyle='dashed', linewidth=2, label='95% CI Lower Bound')
plt.axvline(ci[1], color='red', linestyle='dashed', linewidth=2, label='95% CI Upper Bound')
plt.title('Bootstrapped Distribution of LLM Pricing Improvement')
plt.xlabel('MAPE Reduction (%)')
plt.ylabel('Frequency')
plt.legend()
plt.tight_layout()
plt.savefig("llm_evaluation_bounds.png", dpi=300)